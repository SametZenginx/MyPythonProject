# Addition, Subtraction, Multiplication, Division, Exponential.
def Addition(a,b):
    """
    This function adds Addition two numbers.
    """
    return a+b
def Subtraction(a,b):
    """
    This function subtracts two numbers from each other.
    """
    if a > b:
        return a-b
    else:
        return b-a
def Multiplication(a,b):
    """
    This function multiples two numbers.
    """
    return a*b
def Division(a,b):
    """
    This function divides two numbers. 
    """
    return a/b
def Exponential(a,b):
    """
    This function 1.Number * 1.Number * ...("2.Number") does the operation.
    """
    return a**b
